def test_ingest_dedupes_identical_content():
    conn = _fresh()
    client = _capture_client([{"statement": "s", "discipline": "mep",
                               "rationale": "r", "assumptions": [], "author": []}])
    first = ingest_note(conn, NOTE, path="inbox/a.md", client=client)
    again = ingest_note(conn, NOTE, path="inbox/renamed.md", client=None)  # no LLM call
    assert first["duplicate"] is False and again["duplicate"] is True
    assert again["episode"] == first["episode"]
    assert len(get_all_decisions(conn)) == 1        # captured once, not twice


def test_parse_frontmatter_is_tolerant():
    assert parse_frontmatter("no frontmatter") == ({}, "no frontmatter")
    fm, body = parse_frontmatter("---\nproject: x\n---\nbody")
    assert fm == {"project": "x"} and body == "body"
    fm, _ = parse_frontmatter("---\n:bad yaml [\n---\nbody")
    assert fm == {}


