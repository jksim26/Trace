# ── ingestion ────────────────────────────────────────────────────────────────

def _tool_call(args: dict):
    return SimpleNamespace(function=SimpleNamespace(arguments=json.dumps(args)))


def _capture_client(decisions: list[dict], extra_texts: list[str] = ()):
    """First call returns record_decision tool-calls; later calls return the
    queued plain texts (premise check / court roles)."""
    queue = list(extra_texts)
    calls = {"n": 0}

    def create(**kw):
        calls["n"] += 1
        if calls["n"] == 1:
            msg = SimpleNamespace(content="", tool_calls=[_tool_call(a) for a in decisions])
        else:
            msg = SimpleNamespace(content=queue.pop(0), tool_calls=None)
        return SimpleNamespace(choices=[SimpleNamespace(message=msg)])
    return SimpleNamespace(chat=SimpleNamespace(completions=SimpleNamespace(create=create)))


NOTE = """---
project: demo
date: 2026-02-01
---
## Site meeting
We will fix the north facade render this quarter.
"""


def _fresh():
    conn = connect(":memory:")
    init_db(conn)
    return conn


def test_ingest_stores_episode_and_decision_with_provenance():
    conn = _fresh()
    client = _capture_client([{"statement": "Repair the north facade render",
                               "discipline": "facade", "rationale": "spalling found",
                               "assumptions": ["scope per survey"], "author": ["H. Nair"]}])
    out = ingest_note(conn, NOTE, path="inbox/meeting.md", client=client)
    assert out["episode"] == "E-001" and out["duplicate"] is False
    d = get_decision(conn, out["captured"][0]["id"])
    assert d.source_episode == "E-001"              # provenance is a real reference
    assert d.valid_from == "2026-02-01T00:00:00Z"   # the note's date, not ingest time
    assert d.status == "valid"
    assert verify_audit_chain(conn)[0] is True      # ingest + add, all on the chain


def test_ingest_routes_conflicts_through_the_court():
    conn = _fresh()
    add_decision(conn, Decision(statement="Facade = non-combustible rainscreen",
                                discipline="facade", recorded_at="2026-01-01T00:00Z"))
    client = _capture_client(
        [{"statement": "Swap facade to PE-core ACP", "discipline": "facade",
          "rationale": "cost", "assumptions": [], "author": ["contractor"],
          "cladding_combustible": True}],
        extra_texts=["proposer prose", "guardian prose", "judge prose"])
    out = ingest_note(conn, NOTE, client=client,
                      context={"building": {"height_m": 95, "boundary_distance_m": 7.5}})
    assert out["alerts"] and out["verdicts"]
    proposal = get_decision(conn, out["captured"][0]["id"])
    assert proposal.status == "rejected"            # the court's fate on the row


